def t(key: str, lang: str = "de", **kwargs: Any) -> str:
    table = _STRINGS.get(lang) or _STRINGS["de"]
    template = table.get(key) or _STRINGS["de"].get(key, key)
    return template.format(**kwargs) if kwargs else template


def resolve_ui_language(db: Session, user_id: Optional[int]) -> str:
    """Look up an account's UI language for tasks that only have a user_id
    (e.g. background sync), not the full current_user object."""
    if user_id:
        from app import models
        user = db.query(models.User).filter(models.User.id == user_id).first()
        if user and user.ui_language in ("de", "en"):
            return user.ui_language
    return "de"
